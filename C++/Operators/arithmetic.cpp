#include <iostream>
using namespace std;

// Arithmetic Operators
int main()
{
    int a = 10;
    int b = 10;

    cout << "The Value Of A + B Is : " << a + b << endl;
    cout << "The Value Of A - B Is : " << a - b << endl;
    cout << "The Value Of A * B Is : " << a * b << endl;
    cout << "The Value Of A / B Is : " << a / b << endl;
    cout << "The Value Of A % B Is : " << a % b << endl;

    cout << "The Value Of A++ Is : " << a++ << endl;
    cout << "The Value Of A-- Is : " << a-- << endl;

    cout << "The Value Of ++A Is : " << ++a << endl;
    cout << "The Value Of --A Is : " << --a << endl;

    return 0;
}
