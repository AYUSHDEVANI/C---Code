#include<iostream>
using namespace std;

int main()
{
    int num1,num2;
    cout<<"Enter num1 value : ";
    cin>>num1;
    // '<<' is called insertion operator

    cout<<"Enter num2 value : ";
    cin>>num2;
    // '>>' is called Extraction operator

    cout<<"The sum is "<<num1+num2;

    return 0;
}